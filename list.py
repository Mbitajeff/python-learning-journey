# Creating a list
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Adding elements
fruits.append("orange")
print("After Append:", fruits)

# Removing elements
fruits.remove("banana")
print("After Remove:", fruits)

# Modifying elements
fruits[0] = "avocado"
print("After Modification:", fruits)

# Slicing a list
print("First Two Fruits:", fruits[:2])
